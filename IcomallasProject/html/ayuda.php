        <h2>Registrar Empleado</h2>
        <div class="centrar-imagen"><img src="../images/ayuda/1.png" class="imagen-ayuda"/></div>
        <br>    
        1 -El administrador Ingresa a la interfaz de registro de empleado  a través de la etiqueta registro que se encuentra en el menú principal del sistema.
		<br>
        2 - El sistema abre la interfaz de registro de empleado.
		<br>
        3 - El administrador ingresa el número de identificación del empleado.
		<br>
        4 - El administrador da clic en el botón de generar empleado.
		<br>
        5 - El sistema valida la existencia del empleado verificando en la base de datos, si el empleado no existe genera un formulario de registro.
		<br>
        6 - El administrador ingresa los datos de empleado. 
		<br>
        7 - El administrador da clic en el botón de guardar.
		<br>
        8 - El sistema guarda en su respectiva base de datos la información y dispara el mensaje "datos guardados con éxito".
		<br>
        9 - El sistema limpia los campos de registro de empleado.
